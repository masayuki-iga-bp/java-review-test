package org.example;

public class Caluculator {
    /**
     * 割り算を行います.
     * @param a 割られる数
     * @param b 割る数
     * @return 割り算の結果
     */
    public int divide(int a, int b) {
        return a / b;
    }
    /**
     * 足し算を行います.
     * @param a 足される数
     * @param b 足す数
     * @return 足し算の結果
     */
    public int add(int a, int b) {
        return a + b;
    }
}
